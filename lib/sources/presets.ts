function makePreset(input: {
  id: string;
  name: string;
  group: SourcePresetGroup;
  format: "rss" | "atom" | "json";
  url: string;
  coverage: string;
  tags: string[];
  entities: string[];
  adapter?: "feed" | "json-api";
  access?: "feed" | "api";
  priority?: "M1" | "M2" | "optional";
  refreshIntervalMinutes?: number;
  quality?: "high" | "medium";
}): SourcePreset {
  return {
    adapter: input.adapter ?? "feed",
    access: input.access ?? "feed",
    free: true,
    verified: true,
    priority: input.priority ?? "M1",
    refreshIntervalMinutes: input.refreshIntervalMinutes ?? 120,
    quality: input.quality ?? "high",
    ...input
  };
}
