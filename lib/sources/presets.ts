export function getDefaultSourcePresets(): SourcePreset[] {
  const priorityById = new Map([
    "hacker-news-rss",
    "lobsters-rss",
    "hugging-face-blog",
    "google-ai-blog",
    "aws-machine-learning-blog",
    "ollama-blog",
    "cloudflare-blog",
    "ruanyifeng-weekly",
    "infoq-cn",
    "meituan-tech",
    "solidot",
    "arxiv-cs-ai",
    "arxiv-cs-cl",
    "arxiv-cs-lg"
  ].map((id, index) => [id, index]));

  return verifiedFreeSourcePresets
    .filter((source) => source.group === "default" || source.group === "release" || source.group === "api")
    .sort((left, right) => {
      const leftPriority = priorityById.get(left.id) ?? Number.MAX_SAFE_INTEGER;
      const rightPriority = priorityById.get(right.id) ?? Number.MAX_SAFE_INTEGER;

      return leftPriority - rightPriority || left.name.localeCompare(right.name);
    });
}

export function getSourcePresetsByGroup(): Record<SourcePresetGroup, SourcePreset[]> {
  return {
    default: verifiedFreeSourcePresets.filter((source) => source.group === "default"),
    release: verifiedFreeSourcePresets.filter((source) => source.group === "release"),
    api: verifiedFreeSourcePresets.filter((source) => source.group === "api"),
    optional: verifiedFreeSourcePresets.filter((source) => source.group === "optional")
  };
}

